  /**
   * Tests the case when null training point data is provided.
   */
  @Test
  public void testControllerNullInputOne() {
    FileReader inp1 = null;
    StringReader st1 = new StringReader("linear\n");
    try {
      Controller c = new Controller(inp1, st1);
    } catch (IllegalArgumentException e) {
      assertEquals("Inputs cannot be null", e.getMessage());
    }
  }

